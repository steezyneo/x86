AGAIN:
MOV SI,0
BACK:
MOV AL,[SI]
CMP AL,[SI+1]
JC NEXT
XCHG AL,[SI+1]
MOV [SI],AL
NEXT:
INC SI
LOOP BACK
DEC DX
JNZ AGAIN
