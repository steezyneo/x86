ASSUME CS:CODE, DS:DATA

DATA SEGMENT
LIST DB 53H.05H,27H,38H,43H,87H
COUNT DW 06H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
MOV CX,0000
MOV DX,0000
AGAIN:
MOV SI,0
MOV CX,DX
BACK:
MOV AL,[SI]
CMP AL,[SI+1]
JNC NEXT
XCHG AL,[SI+1]
MOV [SI],AL
NEXT:
INC SI
LOOP BACK
DEC DX
JNZ AGAIN
INT 03H
CODE ENDS
END START
